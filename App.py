import streamlit as st

# إعدادات الصفحة العامة للمنصة
st.set_page_config(
    page_title="Al-Ahli Stroke Command Center",
    page_icon="🧠",
    layout="wide",
    initial_sidebar_state="expanded"
)

# العنوان الرئيسي للمنصة
st.title("🧠 منصة مستشفى الأهلي المتقدمة لتدبير السكتة الدماغية الحادة")
st.markdown("---")

# تقسيم الشاشة إلى عمودين رئيسيين: الأيمن للتقييم والأيسر للقرارات والعلاج
col_eval, col_action = st.columns([1.2, 1])

# ==============================================================================
# العمود الأيمن: التقييم السريري، الوقت، ومقياس NIHSS الكامل
# ==============================================================================
with col_eval:
    st.header("📋 1. التقييم السريري والنافذة الزمنية")
    
    # تحديد الوقت والضغط والوزن
    onset_time = st.number_input("الوقت المنقضي منذ بداية الأعراض / Last Known Well (بالساعات):", min_value=0.0, max_value=168.0, value=2.0, step=0.5)
    patient_weight = st.number_input("وزن المريض الحالي (كغم) - حاسم لحساب الجرعة بدقة:", min_value=30, max_value=200, value=70, step=1)
    sbp = st.number_input("الضغط الانقباضي الحالي للمريض (SBP mmHg):", min_value=50, max_value=260, value=170, step=5)
    dbp = st.number_input("الضغط الانبساطي الحالي للمريض (DBP mmHg):", min_value=30, max_value=180, value=100, step=5)
    
    st.markdown("---")
    st.header("📝 2. مقياس السكتة الدماغية للمعهد الوطني للصحة (NIHSS)")
    st.caption("يرجى تقييم المريض في النقاط الـ 11 التالية لحساب النتيجة الإجمالية وتحديد شدة الحالة:")

    # الأسئلة الـ 11 كاملة وبخياراتها السريرية المعتمدة
    n1 = st.radio("1a. مستوى الوعي (LOC):", (0, 1, 2, 3), format_func=lambda x: {0:"0: متيقظ تماماً", 1:"1: نعاس / استجابة للمنبه الخفيف", 2:"2: استجابة للمنبه الشديد أو المؤلم فقط", 3:"3: غيبوبة / منعكسات حركية فقط"}[x])
    n2 = st.radio("1b. أسئلة مستوى الوعي (الشهر الحالي والعمر):", (0, 1, 2), format_func=lambda x: {0:"0: يجيب على السؤالين بشكل صحيح", 1:"1: يجيب على سؤال واحد بشكل صحيح", 2:"2: لا يجيب على أي سؤال بشكل صحيح"}[x])
    n3 = st.radio("1c. أوامر مستوى الوعي (فتح وإغلاق العينين، قبض وبسط اليد):", (0, 1, 2), format_func=lambda x: {0:"0: ينفذ الأمرين بشكل صحيح", 1:"1: ينفذ أمراً واحداً بشكل صحيح", 2:"2: لا ينفذ أي أمر بشكل صحيح"}[x])
    n4 = st.radio("2. حركة العين الأفقية (Best Gaze):", (0, 1, 2), format_func=lambda x: {0:"0: طبيعي", 1:"1: شلل جزئي في حركة العين", 2:"2: انحراف كامل وثابت للعينين"}[x])
    n5 = st.radio("3. الساحة البصرية (Visual Fields):", (0, 1, 2, 3), format_func=lambda x: {0:"0: لا يوجد نقص", 1:"1: نقص هلالي جزئي (Quadrantallanopsia)", 2:"2: عمى شقي كامل (Hemianopsia)", 3:"3: عمى كامل أو ثنائي الجانب"}[x])
    n6 = st.radio("4. شلل الوجه (Facial Palsy):", (0, 1, 2, 3), format_func=lambda x: {0:"0: حركة طبيعية متناظرة", 1:"1: ضعف طفيف (تسطح الطية الأنفية الشفوية)", 2:"2: شلل جزئي (ضعف الجزء السفلي من الوجه)", 3:"3: شلل كامل (الجزء العلوي والسفلي من جهة واحدة)"}[x])
    
    st.subheader("الأطراف (Motor Tasks)")
    n7 = st.radio("5a & 5b. ضعف الأطراف العلوية (ضعف الذراع الأشد):", (0, 1, 2, 3, 4), format_func=lambda x: {0:"0: لا سقوط (يحافظ على الذراع بزاوية 90 درجة لمدة 10 ثوانٍ)", 1:"1: سقوط تدريجي قبل الـ 10 ثوانٍ دون ملامسة السرير", 2:"2: يسقط الذراع ويلامس السرير ولكن يقاوم الجاذبية جزئياً", 3:"3: يسقط الذراع فوراً، ولا يقاوم الجاذبية بل يتحرك على السرير فقط", 4:"4: لا توجد أي حركة على الإطلاق"}[x])
    n8 = st.radio("6a & 6b. ضعف الأطراف السفلية (ضعف الساق الأشد):", (0, 1, 2, 3, 4), format_func=lambda x: {0:"0: لا سقوط (يحافظ على الساق بزاوية 30 درجة لمدة 5 ثوانٍ)", 1:"1: سقوط تدريجي قبل الـ 5 ثوانٍ دون ملامسة السرير", 2:"2: تسقط الساق وتلامس السرير ولكن تقاوم الجاذبية جزئياً", 3:"3: تسقط الساق فوراً، ولا تقاوم الجاذبية بل تتحرك على السرير فقط", 4:"4: لا توجد أي حركة على الإطلاق"}[x])
    
    st.subheader("التنسيق والوظائف العليا")
    n9 = st.radio("7. رنح الأطراف / التناسق (Limb Ataxia):", (0, 1, 2), format_func=lambda x: {0:"0: لا يوجد رنح (اختبار الإصبع-الأنف والكعب-الساق طبيعي)", 1:"1: موجود في طرف واحد (علوي أو سفلي)", 2:"2: موجود في طرفين أو أكثر"}[x])
    n10 = st.radio("8. الإحساس (Sensory):", (0, 1, 2), format_func=lambda x: {0:"0: طبيعي، لا يوجد نقص في الإحساس", 1:"1: نقص إحساس خفيف إلى متوسط (يشعر بالوخز ولكن بشكل أقل)", 2:"2: فقدان إحساس كامل أو شديد (لا يشعر بالوخز إطلاقاً)"}[x])
    n11 = st.radio("9. اللغة / الحبسة الكلامية (Best Language):", (0, 1, 2, 3), format_func=lambda x: {0:"0: طبيعي، يفهم ويتكلم بسلاسة", 1:"1: حبسة خفيفة إلى متوسطة (يمكن فهمه بصعوبة أو يفهم جزئياً)", 2:"2: حبسة شديدة (الكلام مكسر جداً أو لا يفهم التوجيهات إطلاقاً)", 3:"3: حبسة كاملة / خرس (Global Aphasia)"}[x])
    n12 = st.radio("10. عسر التلفظ / النطق (Dysarthria):", (0, 1, 2), format_func=lambda x: {0:"0: طبيعي، نطق الكلمات واضح تماماً", 1:"1: عسر نطق خفيف إلى متوسط (تداخل طفيف في مخارج الحروف)", 2:"2: عسر نطق شديد جداً (كلام غير مفهوم إطلاقاً أو خرس)"}[x])
    n13 = st.radio("11. الإهمال / عدم الانتباه (Extinction and Inattention):", (0, 1, 2), format_func=lambda x: {0:"0: لا يوجد إهمال جانب، طبيعي", 1:"1: إهمال جزئي (لا ينتبه للمنبهات البصرية أو الحسية في جهة واحدة عند تنبيه الجهتين معاً)", 2:"2: إهمال كامل وجلي (لا يتعرف على يده أو ينتبه لجهة واحدة فقط)"}[x])

    # حساب المجموع
    total_nihss = n1 + n2 + n3 + n4 + n5 + n6 + n7 + n8 + n9 + n10 + n11 + n12 + n13

# ==============================================================================
# العمود الأيسر: قرارات الأشعة، موانع tPA، الحاسبة الذكية، والخطط الممتدة
# ==============================================================================
with col_action:
    st.header("🧠 3. لوحة توجيه الأشعة والقرارات الذكية")
    
    # تصنيف شدة الـ NIHSS بمؤشر مرئي وجذاب
    if total_nihss == 0:
        st.metric(label="إجمالي دَرَجَة NIHSS", value=f"{total_nihss} (طبيعي)")
    elif total_nihss <= 4:
        st.metric(label="إجمالي دَرَجَة NIHSS", value=f"{total_nihss} (سكتة خفيفة - Minor)")
    elif total_nihss <= 15:
        st.metric(label="إجمالي دَرَجَة NIHSS", value=f"{total_nihss} (سكتة متوسطة - Moderate)")
    elif total_nihss <= 20:
        st.metric(label="إجمالي دَرَجَة NIHSS", value=f"{total_nihss} (سكتة متوسطة إلى شديدة)")
    else:
        st.metric(label="إجمالي دَرَجَة NIHSS", value=f"{total_nihss} (سكتة شديدة وحادة جداً)")
        st.warning("⚠️ تنبيه البورد المتقدم: درجة NIHSS أعلى من 25 تزيد بشكل كبير جداً من خطر التحول النزفي (Hemorrhagic Transformation) بعد الـ tPA. وازن المنافع والمخاطر بدقة.")

    st.markdown("---")
    
    # مسار تصوير الدماغ والـ Windows الزمنية
    st.subheader("🎥 بروتوكول التصوير العصبي المقترح:")
    ct_scan = st.radio("نتيجة التصوير الطبقي للدماغ الفوري (Non-Contrast CT Brain):", 
                       ("آمن: لا يوجد دليل على نزيف حاد أو احتشاء مبكر واسع", 
                        "خطر: يوجد نزيف دماغي حاد (ICH)", 
                        "خطر: احتشاء حاد واسع يغطي أكثر من ثلث الشريان المخي الأوسط (>1/3 MCA)"))
    
    # معالجة القرارات بناءً على الوقت والأشعة
    eligible_for_tpa = False
    
    if "يوجد نزيف" in ct_scan or "احتشاء حاد واسع" in ct_scan:
        st.error("🔴 توقف فوراً! إعطاء مذيب الجلطة (tPA) ممنوع منعا باتاً بناءً على نتائج الأشعة الحالية لحماية المريض.")
    else:
        if onset_time <= 4.5:
            st.success(f"🟢 المريض ضمن النافذة الزمنية الذهبية لمذيب الجلطة الوريدي ({onset_time} ساعات).")
            eligible_for_tpa = True
        elif onset_time > 4.5 and onset_time <= 24.0:
            st.info("🔍 المريض في النافذة الممتدة (4.5 - 24 ساعة): اطلب فوراً تصوير الأوعية الطبقي (CT Angiography) للبحث عن انسداد الشرايين الكبيرة (LVO). المريض مرشح للتحويل العاجل لاستئصال الخثرة الميكانيكي (Mechanical Thrombectomy).")
        else:
            st.error("⚪ المريض خارج النوافذ الزمنية للعلاجات الحادة (>24 ساعة). اذهب مباشرة لمسار الحماية العصبية والوقاية الثانوية.")

    # فحص موانع الإعطاء المطلقة إذا كان الوقت ملائماً والأشعة سليمة
    if eligible_for_tpa:
        st.markdown("---")
        st.subheader("🛑 التحقق من موانع الاستطباب المطلقة للـ tPA:")
        st.caption("يجب تأكيد خلو التاريخ المرضي للمريض من النقاط التالية بـ (لا):")
        
        contra_1 = st.checkbox("هل تعرض المريض لنزيف دماغي سابق (ICH) أو ورم أو تشوه شرياني وريدي دماغي؟")
        contra_2 = st.checkbox("هل خضع المريض لجراحة كبرى (صدرية أو بطنية) أو تعرض لرضخ رأس شديد خلال الـ 14 يوماً الماضية؟")
        contra_3 = st.checkbox("هل يعاني المريض من نزيف داخلي نشط أو اشتباه قوي بنزيف تحت العنكبوتية (SAH)؟")
        contra_4 = st.checkbox("هل يعاني المريض من أهبة نزفية شديدة (مثلاً صفائح أقل من 100,000)؟")
        
        if contra_1 or contra_2 or contra_3 or contra_4:
            st.error("❌ تم إغلاق مسار الـ tPA لوجود مانع استطباب مطلق لحماية حياة المريض.")
            eligible_for_tpa = False

    # فحص وضبط الضغط الشرياني
    if eligible_for_tpa:
        st.markdown("---")
        st.subheader("🩸 إدارة الضغط الشرياني السريع قبل الـ tPA:")
        if sbp > 185 or dbp > 110:
            st.warning(f"⚠️ الضغط الحالي ({sbp}/{dbp}) مرتفع جداً وممنوع إعطاء tPA قبل إنزاله تحت 185/110 mmHg.")
            st.markdown("""
            **البروتوكول المحلي المقيد بمستشفى الأهلي (Labetalol):**
            * أعطِ **Labetalol 10-20 mg** وريدياً على مدار 1-2 دقيقة.
            * يمكن تكرار الجرعة مرة واحدة بعد 10 دقائق.
            * *إذا لم ينزل الضغط تحت 185/110 بعد المحاولة الثانية، ألغِ مسار الـ tPA تماماً لتجنب النزيف.*
            """)
            eligible_for_tpa = False
        else:
            st.success(f"✅ الضغط الحالي ({sbp}/{dbp}) مناسب وآمن لبدء العلاج بمذيب الجلطة.")

    # حاسبة الجرعات الذكية والأوامر الفورية للمقيم
    if eligible_for_tpa and patient_weight > 0:
        st.markdown("---")
        st.subheader("💉 4. حاسبة جرعات Alteplase (tPA) الفورية والأوامر الطبية")
        
        # الحساب الدقيق للجرعة (0.9 ملغم/كغم بحد أقصى 90 ملغم)
        calculated_dose = patient_weight * 0.9
        total_dose = min(calculated_dose, 90.0)
        bolus_dose = total_dose * 0.10
        infusion_dose = total_dose * 0.90
        
        # عرض النتائج بشكل بارز جداً لمنع أي لغط طبي في التمريض
        st.info(f"📊 **الوزن المعتمد:** {patient_weight} كغم | **الجرعة الإجمالية:** {total_dose:.1f} ملغم")
        
        st.markdown(f"""
        ### 📋 بروتوكول إعطاء الدواء الفوري:
        1. 💉 **جرعة البولس الفورية (Bolus - 10%):** أعطِ **{bolus_dose:.1f} ملغم** وريدياً بشكل مباشر (IV Push) على مدار **دقيقة واحدة كاملة**.
        2. 💧 **جرعة التسريب المستمر (Infusion - 90%):** قم بتسريب **{infusion_dose:.1f} ملغم** المتبقية عبر مضخة التسريب الإلكترونية على مدار **60 دقيقة كاملة**.
        """)
        
        # خطة الرعاية المتكاملة الممتدة لـ 24 ساعة
        st.markdown("---")
        st.subheader("📋 5. خطة أوامر الرعاية الممتدة لأول 24 ساعة (Post-tPA Order Set)")
        st.markdown(f"""
        * **📊 مراقبة العلامات الحيوية والفحص العصبي الصارم:**
          * كل 15 دقيقة خلال أول ساعتين من بدء الـ tPA.
          * كل 30 دقيقة خلال الـ 6 ساعات التالية.
          * كل ساعة كاملة حتى مرور 24 ساعة من انتهاء الجرعة.
        * **🩸 هدف الضغط الشرياني المستهدف (Post-tPA Target):**
          * الحفاظ على الضغط **أقل من 180/105 mmHg** بشكل صارم لمنع النزيف التحولي الإقفاري.
        * **🚫 المحظورات الصارمة والمطلقة (خلال الـ 24 ساعة الأولى):**
          * يمنع منعاً باتاً إعطاء الأسبرين (Aspirin)، بلافيكس (Clopidogrel)، أو الهيبارين (Heparin).
          * يمنع تركيب قساطر وريدية مركزية أو قساطر بولية أو أنابيب تغذية (NG Tube) خلال أول 24 ساعة إلا للضرورة القصوى لتجنب النزيف.
        * **🔬 التصوير التأكيدي المجدول:**
          * اطلب روتينياً **Non-Contrast CT Brain** تأكيدي بعد مرور 24 ساعة من الـ tPA **وقبل** البدء بأول جرعة مميع أو مضاد صفائح.
        """)
        
        # لفتة البورد التعليمية المتقدمة
        st.markdown("""
        > 💡 **لفتة البورد المتقدمة (Board Pearl) المدمجة:**
        > *إذا ساءت الحالة العصبية للمريض فجأة أثناء تسريب الـ tPA (مثل: صداع حاد، قيء قذفي، تدهور سريع في درجة الوعي)، أوقف التسريب فوراً واطلب تصوير طبقي للدماغ بشكل عاجل جداً، وحضّر 10 وحدات Cryoprecipitate للتعامل مع النزيف التحولي المباشر.*
        """)
